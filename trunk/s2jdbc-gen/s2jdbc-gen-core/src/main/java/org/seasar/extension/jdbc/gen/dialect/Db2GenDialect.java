package org.seasar.extension.jdbc.gen.dialect;

import java.math.BigDecimal;
import java.sql.SQLException;
import java.sql.Types;
import java.util.ArrayList;
import java.util.List;

import javax.persistence.GenerationType;

/**
 * DB2の方言を扱うクラスです。
 * 
 * @author taedium
 */
public class Db2GenDialect extends StandardGenDialect {

    /** テーブルが見つからないことを示すSQLステート */
    protected static String TABLE_NOT_FOUND_SQL_STATE = "42704";

    /**
     * インスタンスを構築します。
     */
    public Db2GenDialect() {
        typeMap.put(Types.BINARY, Db2Type.BINARY);
        typeMap.put(Types.BOOLEAN, Db2Type.BOOLEAN);
        typeMap.put(Types.BLOB, Db2Type.BLOB);
        typeMap.put(Types.CHAR, Db2Type.CHAR);
        typeMap.put(Types.CLOB, Db2Type.CLOB);
        typeMap.put(Types.DECIMAL, Db2Type.DECIMAL);
        typeMap.put(Types.LONGVARBINARY, Db2Type.LONGVARBINARY);
        typeMap.put(Types.LONGVARCHAR, Db2Type.LONGVARCHAR);
        typeMap.put(Types.REAL, Db2Type.REAL);
        typeMap.put(Types.TINYINT, Db2Type.TINYINT);
        typeMap.put(Types.VARBINARY, Db2Type.VARBINARY);
    }

    @Override
    public String getDefaultSchemaName(String userName) {
        if (userName == null) {
            return null;
        }
        return userName.toUpperCase();
    }

    @Override
    public GenerationType getDefaultGenerationType() {
        return GenerationType.IDENTITY;
    }

    @Override
    public boolean supportsSequence() {
        return true;
    }

    @Override
    public String getSequenceDefinitionFragment(String dataType, int initValue,
            int allocationSize) {
        return "as " + dataType + " start with " + allocationSize
                + " increment by " + initValue;
    }

    @Override
    public String getIdentityColumnDefinition() {
        return "generated by default as identity";
    }

    @Override
    public boolean isTableNotFound(Throwable t) {
        for (SQLException e : getAllSQLExceptions(t)) {
            if (TABLE_NOT_FOUND_SQL_STATE.equals(e.getSQLState())) {
                return true;
            }
        }
        return false;
    }

    /**
     * 原因となった、もしくは関連付けられたすべての{@link SQLException}を取得します。
     * 
     * @param t
     *            例外
     * @return 原因となった、もしくは関連付けられた{@link SQLException}のリスト
     */
    protected List<SQLException> getAllSQLExceptions(Throwable t) {
        List<SQLException> sqlExceptionList = new ArrayList<SQLException>();
        while (t != null) {
            if (t instanceof SQLException) {
                SQLException cause = SQLException.class.cast(t);
                sqlExceptionList.add(cause);
                if (cause.getNextException() != null) {
                    cause = cause.getNextException();
                    sqlExceptionList.add(cause);
                    t = cause;
                    continue;
                }
            }
            t = t.getCause();
        }
        return sqlExceptionList;
    }

    /**
     * DB2用の{@link Type}の実装です。
     * 
     * @author taedium
     */
    public static class Db2Type extends StandardType {

        private static Type BINARY = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return byte[].class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                if ("char () for bit data".equalsIgnoreCase(typeName)) {
                    return format("char(%d) for bit data", length);
                }
                return format("varchar(%d) for bit data", length);
            }
        };

        private static Type BOOLEAN = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return Boolean.class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return "smallint";
            }
        };

        private static Type BLOB = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return byte[].class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return format("blob(%d)", length);
            }
        };

        private static Type CHAR = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                if (length > 1) {
                    return String.class;
                }
                return Character.class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                if (length > 254) {
                    return "char(254)";
                }
                return format("char(%d)", length);
            }
        };

        private static Type CLOB = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return String.class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return format("clob(%d)", length);
            }
        };

        private static Type DECIMAL = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return BigDecimal.class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return format("numeric(%d,%d)", precision, scale);
            }
        };

        private static Type LONGVARBINARY = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return byte[].class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return "long varchar for bit data";
            }
        };

        private static Type LONGVARCHAR = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return String.class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return "long varchar";
            }
        };

        private static Type REAL = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return Double.class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return "real";
            }
        };

        private static Type TINYINT = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return Short.class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return "smallint";
            }
        };

        private static Type VARBINARY = new Db2Type() {

            @Override
            public Class<?> getJavaClass(int length, int precision, int scale,
                    String typeName) {
                return byte[].class;
            }

            @Override
            public String getColumnDefinition(int length, int precision,
                    int scale, String typeName) {
                return format("varchar(%d) for bit data", length);
            }
        };

        /**
         * インスタンスを構築します。
         */
        protected Db2Type() {
        }

    }
}
